package com.feka.ubed_patient.model;

public class Count {
    private String type;
    private int number;

    public Count() {
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
